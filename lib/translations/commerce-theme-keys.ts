// Helper function to get all translation keys for CSV export
export function getCommerceTranslationKeys() {
  const keys: Array<{namespace: string, key: string, defaultValue: string}> = [];
  
  Object.entries(commerceThemeTranslations).forEach(([namespace, translations]) => {
    Object.entries(translations).forEach(([key, value]) => {
      if (typeof value === 'object') {
        // Handle nested objects
        Object.entries(value).forEach(([subKey, subValue]) => {
          keys.push({
            namespace,
            key: `${key}.${subKey}`,
            defaultValue: subValue as string
          });
        });
      } else {
        keys.push({
          namespace,
          key,
          defaultValue: value as string
        });
      }
    });
  });
  
  return keys;
}